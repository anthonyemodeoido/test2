h1. Formatting for forums
<span class="padding-top"><br></span><h2>Basic formatting</h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p>Use *bold* for <b>bold</b> text, _italic_ - for <i>italic</i> text.</p><span
        class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p>Start line with &gt; sign for quote, like this:<br>
    &gt; this will be quote</p><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Referencing</h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p>To mention another Habbo simply use the format '@Habboname'. This will be turned
    into hyper-link to the Habbo's profile, and the Habbo will receive a notification (not fully implemented yet).</p>
<span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><h2>Replying</h2><span class="padding-bottom"><br></span>
<span class="padding-top"><br></span><p>You can reply to individual messages by clicking green &quot;reply&quot; button
    in message bar. You can also reply to lots of messages at once, by keeping the &quot;compose message&quot; window
    open and clicking more &quot;reply&quot; buttons in forum view.</p><span class="padding-bottom"><br></span>